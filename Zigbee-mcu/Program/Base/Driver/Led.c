#define LED_C_

/* ------------------------------------------------------------------------------------------------
 *                                          Includes
 * ------------------------------------------------------------------------------------------------
 */
#include "led.h"



/**************************************************************************************************
 * @fn          Led_Init
 * @brief       灯初始化
 * @param       无
 * @return      无
 **************************************************************************************************
 */
void Led_Init(void)
{
  P1SEL &= ~0x03;   //设置P1.0、P1.1为通用I/O口  | PXSEL: 0.通用 1.外设
  P1DIR |=  0x03;   //设置P1.0、P1.1为输出       | PXDIR: 0:输入 1.输出
  P1INP &= ~0x03;   //设置成上拉，也可以起到限流作用 
}



