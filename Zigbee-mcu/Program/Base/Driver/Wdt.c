#include "Wdt.h"



/************************************************
 *function :  Wdt_Init
 *describe :  开门狗初始化
 *input    :  无
 *return   :  无
 ***********************************************/
void Wdt_Init(void)
{
  WDCTL  = 0x00;    
  WDCTL |= 0x08;    //定时器间隔选择，间隔1S
}

/************************************************
 *function :  Wdt_FeetDog
 *describe :  开门狗计数清零
 *input    :  无
 *return   :  无
 ***********************************************/
void Wdt_FeetDog(void)
{
  WDCTL  = 0xA0;    
  WDCTL |= 0x50;    //定时器间隔选择，间隔1S
}