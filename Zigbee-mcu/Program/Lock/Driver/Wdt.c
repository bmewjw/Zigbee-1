#include "Wdt.h"


/**************************************************************************************************
 * @fn          Wdt_Init
 * @brief       开门狗初始化
 * @param       无
 * @return      无
 **************************************************************************************************
 */
void Wdt_Init(void)
{
  WDCTL  = 0x00;    
  WDCTL |= 0x08;    //定时器间隔选择，间隔1S
}


/**************************************************************************************************
 * @fn          Wdt_FeetDog
 * @brief       开门狗计数清零
 * @param       无
 * @return      无
 **************************************************************************************************
 */
void Wdt_FeetDog(void)
{
  WDCTL  = 0xA0;    
  WDCTL |= 0x50;    //定时器间隔选择，间隔1S
}